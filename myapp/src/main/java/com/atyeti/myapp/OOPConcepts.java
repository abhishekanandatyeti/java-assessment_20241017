package com.atyeti.myapp;

public class OOPConcepts {
    // Explanations of OOP Concepts

    /*
    Inheritance: One class is allowed to inherit the fields and methods of another class. It means creating new classes based on existing ones. We can add new fields and methods to the inherited class as well.  
    
    Polymorphism: It allows us to perform a single action in different ways. It allows us to define one interface and have multiple implementations. Polymorphism enables us to create more flexible and reusable code.
    
    Encapsulation: It refers to the bundling of data and methods that operate on that data within a single unit (class). It is a way of hiding the implementation details of a class from outside access and only exposing a public interface that can be used to interact with the class.
    
    Abstraction: It is the process in which we only show essential details/functionality to the user.
    */
}
